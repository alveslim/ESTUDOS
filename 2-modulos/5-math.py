import math
#MODULO BUIT IN MATH - FUNCOES MATEMATICAS
print(f"Valor de pi: {math.pi:.2f}")
print(f"{math.e:.2f}") # Valor de euler
num = 16.4
print(math.ceil(num)) #  Arredonda para cima
print(math.floor(num)) # Arredonda para baixo
num2 = 7
print(math.factorial(num2))
print(math.pow(11, 2)) # Potencia
print(math.sqrt(81)) # Raiz quadrada

mdc = math.gcd(25, 100)
print(mdc)